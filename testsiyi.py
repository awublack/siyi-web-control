def crc16(data):
    crc = 0
    for b in data:
        tmp = ((crc >> 8) ^ b) & 0xFF
        crc = ((crc << 8) ^ CRC16_TABLE[tmp]) & 0xFFFF
    return crc

def build_original_pkt(cmd, data_bytes):
    """还原你之前能用的魔改包结构"""
    # '<H B H H B' = 55 66 | 00 | 02 00 | 01 00 | CMD
    pkt = struct.pack('<H B H H B', 0x6655, 0x00, 0x0002, 0x0001, cmd) + data_bytes
    # 原始代码是对包含 Header 的整个 pkt 计算 CRC
    crc = crc16(pkt)
    return pkt + struct.pack('<H', crc)

def build_standard_pkt(cmd, data_bytes):
    """标准 SIYI 协议 (mzahana/siyi_sdk)"""
    length = len(data_bytes)
    seq = 0
    pkt_header = b'\x55\x66'
    pkt_body = struct.pack('<BBB', length, seq, cmd) + data_bytes
    crc = crc16(pkt_body) # 标准协议 CRC 不含 Header
    return pkt_header + pkt_body + struct.pack('<H', crc)

